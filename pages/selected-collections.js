import Head from "next/head";
import Image from "next/image";
import styles from "@/styles/Selected.module.css";
import Layout from "@/components/layout";
import Link from "next/link";
import Header from "@/components/header";
import { useEffect } from "react";
import { useState } from "react";
import { getContract } from "./api/hello";

export default function Home() {
  const [collections, setCollections] = useState([]);

  const getCollections = async () => {
    const contract = await getContract();
    const collectionCount = await contract.lastCollectionId();

    const _collections = [];
    for (let i = 0; i < collectionCount; i++) {
      const collection = await contract.collections(i + 1);
      const name = collection.name;
      const image = collection.image;
      const index = i + 1;
      _collections.push({ name, image, index });
    }

    setCollections(_collections);
  };

  useEffect(() => {
    getCollections();
  }, []);

  return (
    <>
      <Head>
        <title>Collections </title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <div className="left_gradient" />
        <div className="right_gradient" />
        <main className={styles.main}>
          <div className={styles.header}>
            <Header
              title="RAFFLE TOOL FOR SELECTED COLLECTIONS"
              texts={[
                "Anyone can make fair raffles for early starkers via that page. Our raffle is totally on-chain, transparent & accountable. Just as suppose to be!",
              ]}
            />
          </div>
          <div className={styles.collections}>
            {collections.map((collection, index) => (
              <div key={index} className={styles.collection}>
                <Link
                  href={`/collection/${collection.index}`}
                  className={styles.collection_image}
                >
                  <Image src={collection.image} alt="raffle" fill />
                </Link>
                <div className={styles.collection_info}>
                  <h2 className={styles.collection_title}>{collection.name}</h2>
                </div>
              </div>
            ))}
          </div>
          <footer className={styles.footer}>
            <div className={styles.footer_container}>
              <div className={styles.footer_item}>
                <h3 className={styles.footer_title}>
                  Connect For Listing Your Project. It is free
                </h3>
              </div>
            </div>
          </footer>
        </main>
      </Layout>
    </>
  );
}
